package calculatorTask.operations;

public class OperatorHandler {
    public static double getSum(double n1, double n2) {
        return n1 + n2;
    }

    public static double getDivision(double n1, double n2) {
        return n1 / n2;
    }

    public static double getMult(double n1, double n2) {
        return n1 * n2;
    }

    public static double getSubtraction(double n1, double n2) {
        return n1 - n2;
    }


}
